    public void actualizarTropas(int [] refuerzos){
        if(refuerzos == null) return;
        txtLibres.setText(String.valueOf(refuerzos[0]));
        txtAfrica.setText(String.valueOf(refuerzos[1]));
        txtAmericaNorte.setText(String.valueOf(refuerzos[2]));
        txtAmericaSur.setText(String.valueOf(refuerzos[3]));
        txtAsia.setText(String.valueOf(refuerzos[4]));
        txtEuropa.setText(String.valueOf(refuerzos[5]));
        txtOceania.setText(String.valueOf(refuerzos[6]));
    }
    public boolean esAgregar(){
        return rbAgregar.isSelected();
    }
    public int cantidadPaisesDisponibles(){
        return refuerzos[0];
    }

